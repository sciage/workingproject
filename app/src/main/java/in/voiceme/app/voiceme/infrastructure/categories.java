public class categories {
    private byte family;
    private byte health;
    private byte work;
    private byte social;
    private byte others;

    public byte getFamily() {
        return family;
    }

    public void setFamily(byte family) {
        this.family = family;
    }

    public byte getHealth() {
        return health;
    }

    public void setHealth(byte health) {
        this.health = health;
    }

    public byte getWork() {
        return work;
    }

    public void setWork(byte work) {
        this.work = work;
    }

    public byte getSocial() {
        return social;
    }

    public void setSocial(byte social) {
        this.social = social;
    }

    public byte getOthers() {
        return others;
    }

    public void setOthers(byte others) {
        this.others = others;
    }


}
